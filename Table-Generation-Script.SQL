CREATE TABLE LEARNER
(
	USER_ID NUMBER PRIMARY KEY,
	USER_EMAIL VARCHAR2(50),
	USER_PASS VARCHAR2(50)
);


CREATE TABLE ENTITY
(
	ENTITY_ID NUMBER PRIMARY KEY
);

CREATE TABLE ENTITY_LEVEL
(
	ENTITY_LEVEL_ID NUMBER PRIMARY KEY,
	LEVEL_NO NUMBER,
	LEVEL_TITLE VARCHAR2(100),
	ENTITY_TYPE NUMBER(1)
);

CREATE TABLE WORD_GROUP
(
	WORD_GROUP_ID number PRIMARY KEY,
	TITLE VARCHAR2(20),
	LEVEL_ID NUMBER REFERENCES ENTITY_LEVEL(ENTITY_LEVEL_ID)
);



CREATE TABLE WORD
(
	ENTITY_ID NUMBER PRIMARY KEY REFERENCES ENTITY(ENTITY_ID),
	WORD VARCHAR2(50), 
	MEANING VARCHAR2(300), 
	PARTS_OF_SPEECH VARCHAR2(20), 
	WORD_GROUP_ID NUMBER REFERENCES WORD_GROUP(WORD_GROUP_ID)
);


CREATE TABLE LESSON
(
	ENTITY_ID NUMBER PRIMARY KEY REFERENCES ENTITY(ENTITY_ID),
	CREATION_DATE DATE DEFAULT SYSDATE,
	TITLE VARCHAR2(300),
	CONTENT VARCHAR2(2000),
	LEVEL_ID NUMBER REFERENCES ENTITY_LEVEL(ENTITY_LEVEL_ID)
);


CREATE TABLE WORD_RELATION
(
	WORD_RELATION_ID NUMBER PRIMARY KEY,
	RELATION_NAME VARCHAR2(100), 
	WORD_ID1 NUMBER REFERENCES WORD(ENTITY_ID),
	WORD_ID2 NUMBER REFERENCES WORD(ENTITY_ID)
);

CREATE TABLE WORD_EXAMPLE
(
	WORD_EXAMPLE_ID NUMBER PRIMARY KEY,
	ENTITY_ID NUMBER REFERENCES WORD(ENTITY_ID),
	SENTENCE VARCHAR2(300)
);


CREATE TABLE TRANSLATION
(
	TRANSLATION_ID NUMBER PRIMARY KEY,
	ENTITY_ID NUMBER,
	BANGLA VARCHAR2(300),
	ENGLISH_OPTION1 VARCHAR2(300),	
	ENGLISH_OPTION2 VARCHAR2(300),
	ENGLISH_OPTION3 VARCHAR2(300),
	FOREIGN KEY(ENTITY_ID) REFERENCES LESSON(ENTITY_ID)
);


CREATE TABLE PRACTICE
(
	PRACTICE_ID number PRIMARY KEY,
	ENTITY_ID NUMBER,
	PROBLEM VARCHAR2(40),
	OPTION1 VARCHAR2(50),
	OPTION2 VARCHAR2(50),
	OPTION3 VARCHAR2(50),
	FOREIGN KEY(ENTITY_ID) REFERENCES ENTITY(ENTITY_ID)
);

CREATE TABLE LEARNT
(
	USER_ID NUMBER  NOT NULL references LEARNER(USER_ID),
	ENTITY_ID NUMBER  NOT NULL references ENTITY(ENTITY_ID),
	DATE_LEARNT DATE DEFAULT SYSDATE,
	sOLVED_LAST_TIME NUMBER,
	PRIMARY KEY (USER_ID, ENTITY_ID)
);